function getChartDrawingData(metric, points) {
  const chartWidth = 320
  const chartHeight = 180
  const padding = 22
  const hasPoints = points.length > 0
  const values = points.map((point) => point.value)
  const actualMinValue = hasPoints ? Math.min(...values) : 0
  const actualMaxValue = hasPoints ? Math.max(...values) : 1
  const chartMinValue =
    metric.kind === 'status'
      ? 0
      : actualMinValue === actualMaxValue
        ? actualMinValue - Math.max(Math.abs(actualMinValue) * 0.15, 1)
        : actualMinValue
  const chartMaxValue =
    metric.kind === 'status'
      ? 1
      : actualMinValue === actualMaxValue
        ? actualMaxValue + Math.max(Math.abs(actualMaxValue) * 0.15, 1)
        : actualMaxValue
  const range = chartMaxValue - chartMinValue || 1

  const svgPoints = points.map((point, index) => {
    const x =
      points.length === 1
        ? chartWidth / 2
        : padding + (index / (points.length - 1)) * (chartWidth - padding * 2)

    const y =
      chartHeight -
      padding -
      ((point.value - chartMinValue) / range) * (chartHeight - padding * 2)

    return { x, y, ...point }
  })

  const linePath = svgPoints
    .map((point, index) => `${index === 0 ? 'M' : 'L'} ${point.x} ${point.y}`)
    .join(' ')

  const areaPath = svgPoints.length
    ? `${linePath} L ${svgPoints.at(-1).x} ${chartHeight - padding} L ${svgPoints[0].x} ${chartHeight - padding} Z`
    : ''

  const latestPoint = points.at(-1)
  const highestDisplayValue = metric.transform ? metric.transform(actualMaxValue) : actualMaxValue
  const lowestDisplayValue = metric.transform ? metric.transform(actualMinValue) : actualMinValue
  const highestLabel =
    metric.kind === 'status'
      ? 'Pump On'
      : `${highestDisplayValue.toFixed(metric.precision ?? 2)}${metric.unit ? ` ${metric.unit}` : ''}`
  const lowestLabel =
    metric.kind === 'status'
      ? 'Pump Off'
      : `${lowestDisplayValue.toFixed(metric.precision ?? 2)}${metric.unit ? ` ${metric.unit}` : ''}`

  return {
    areaPath,
    chartHeight,
    chartWidth,
    hasPoints,
    highestLabel,
    latestPoint,
    linePath,
    lowestLabel,
    padding,
    svgPoints,
  }
}

