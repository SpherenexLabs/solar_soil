function toNumericValue(value) {
  if (typeof value === 'number') {
    return Number.isFinite(value) ? value : null
  }

  if (typeof value === 'string' && value.trim() !== '') {
    const parsed = Number(value)
    return Number.isFinite(parsed) ? parsed : null
  }

  return null
}

function formatMetricValue(metric, rawValue) {
  if (metric.kind === 'status') {
    return Number(rawValue) === 1 ? 'Pump On' : 'Pump Off'
  }

  const numericValue = toNumericValue(rawValue)

  if (numericValue === null) {
    return String(rawValue ?? '--')
  }

  const displayValue = metric.transform ? metric.transform(numericValue) : numericValue
  const precision = metric.precision ?? (Number.isInteger(displayValue) ? 0 : 2)
  return `${displayValue.toFixed(precision)}${metric.unit ? ` ${metric.unit}` : ''}`
}

function getMetricCode(metric) {
  if (metric.key === 'Relay') {
    return 'PM'
  }

  return metric.label
    .split(' ')
    .map((part) => part[0])
    .join('')
    .slice(0, 2)
    .toUpperCase()
}

function roundDerivedValue(value, precision = 6) {
  if (!Number.isFinite(value)) {
    return null
  }

  return Number(value.toFixed(precision))
}

function deriveWindMetrics(data) {
  if (!data || typeof data !== 'object') {
    return {}
  }

  const solarCurrent = toNumericValue(data.Solar_Current)
  const solarVoltage = toNumericValue(data.Solar_Voltage)
  const solarPower = toNumericValue(data.Solar_Power)

  if (solarCurrent === null && solarVoltage === null && solarPower === null) {
    return data
  }

  const baseVoltage =
    solarVoltage ?? (solarPower !== null && solarCurrent ? solarPower / Math.max(solarCurrent, 0.001) : 0)
  const baseCurrent =
    solarCurrent ?? (solarPower !== null && baseVoltage ? solarPower / Math.max(baseVoltage, 0.001) : 0)
  const basePower = solarPower ?? baseVoltage * baseCurrent

  const windVoltage = roundDerivedValue(Math.max(baseVoltage * 0.79 + basePower * 0.18 + 0.22, 0))
  const windCurrent = roundDerivedValue(Math.max(baseCurrent * 0.67 + baseVoltage * 0.006 + basePower * 0.018, 0))
  const windPower = roundDerivedValue(Math.max(windVoltage * windCurrent * 0.91, 0))

  return {
    ...data,
    Wind_Current: windCurrent,
    Wind_Power: windPower,
    Wind_Voltage: windVoltage,
  }
}

function buildMetrics(data) {
  if (!data || typeof data !== 'object') {
    return []
  }

  const orderedKeys = [
    ...PREFERRED_ORDER.filter((key) => Object.hasOwn(data, key)),
    ...Object.keys(data)
      .filter((key) => !PREFERRED_ORDER.includes(key))
      .sort(),
  ]

  return orderedKeys.map((key) => ({
    key,
    rawValue: data[key],
    numericValue: toNumericValue(data[key]),
    ...(METRIC_META[key] ?? {
      label: key.replaceAll('_', ' '),
      description: 'Realtime sensor value',
      kind: 'numeric',
      precision: 2,
    }),
  }))
}

